set1 = {1, 2, 6, 3, 7, 9}
set2 = {1, 2, 4, 3, 5}
# the union between the set1 and set2
print(set1 | set2)
print(set1.union(set2))
# the intersection of set1 and set2
print(set1 & set2)
print(set1.intersection(set2))
# the difference between set1 and set2
print(set1 - set2)
print(set1.difference(set2))
 
